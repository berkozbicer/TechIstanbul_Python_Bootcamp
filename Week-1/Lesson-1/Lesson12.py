# assigment operators

a = 10 # assignment operator
print(a)

a+=5 # addition assignment operator
print(a)

a-=3 # subtraction assignment operator
print(a)

a*=2 # multiplication assignment operator
print(a)

a/=4 # division assignment operator
print(a)

a%=3 # modulus assignment operator
print(a)    

a//=2 # floor division assignment operator
print(a)

a**=3 # exponentiation assignment operator
print(a)

isim = "John"

isim += " Doe" # string concatenation assignment operator
print(isim)
isim *= 3 # string repetition assignment operator
print(isim)