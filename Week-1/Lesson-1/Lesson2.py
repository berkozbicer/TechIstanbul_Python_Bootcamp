""" Basic Python Syntax """

# int - Integer
# float - Floating Point Number
# Negative numbers are also supported


print(34) # Output: 34
print(-32.5) # Output: -32.5
print(1.77) # Output: 1.77
print(-5)   # Output: -5

print(type(34)) # Output: <class 'int'>
print(type(-32.5)) # Output: <class 'float'>